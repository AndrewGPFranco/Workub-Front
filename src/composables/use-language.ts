export type TranslationKey = keyof typeof messages['pt-BR'];

const language = ref<Language>('pt-BR');

const getPreferredLanguage = (): Language => {
    const storedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);

    if (storedLanguage === 'pt-BR' || storedLanguage === 'en')
        return storedLanguage;

    return navigator.language.toLowerCase().startsWith('en') ? 'en' : 'pt-BR';
};

const applyLanguage = (value: Language) => {
    language.value = value;
    document.documentElement.lang = value;
};

export const initializeLanguage = () => applyLanguage(getPreferredLanguage());

export const translate = (key: TranslationKey) => messages[language.value][key];

export const useLanguage = () => {
    const setLanguage = (value: Language) => {
        localStorage.setItem(LANGUAGE_STORAGE_KEY, value);
        applyLanguage(value);
    };

    return {
        language,
        setLanguage,
        t: translate,
    };
};
